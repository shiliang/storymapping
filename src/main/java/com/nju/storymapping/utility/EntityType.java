package com.nju.storymapping.utility;

public enum EntityType {
    CARD("卡片",1);

    private String name;
    private int index;

    EntityType(String name, int index) {
        this.name = name;
        this.index = index;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }
}
